                    <h3>Peace Building and Advocac</h3>
                    <p>This project area will focus on bringing together youth from diverse and often conflicting ethnic groups—but with shared faith—to interact, dialogue, and return to their communities fully empowered to bridge critical gaps and address some of the country’s most pressing ethnic and tribal conflicts. We will do this by convening workshops, dialogues, debates, and dramas at national universities, civic engagement centers and secondary schools across the country and then disseminating these messages through radio, television, and newspapers. Major conflict prone areas such Wau, Bor, Juba, Malakal will be given preeminence.</p>
