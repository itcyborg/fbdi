                    <h3>Border Humanitarian Response </h3>
                    <p>The ongoing conflict in South Sudan has forced more than 1.5 million people to flee across the borders (Kenya, Uganda, Ethiopia, Sudan, and Congo) while an additional 2 million people are internally displaced. Thousands more continue to cross the borders due to recent intensified fighting between Murle and Dinka and are often stranded without access to food and basic social services. As such, Faith-based Development Initiative seeks to provide these fleeing refugees with cross border humanitarian assistance, psychosocial support, and documentation services necessary for them to resettle in neighboring countries, starting with the Kenya (Nadapal) and Uganda (Kaya) borders. </p>
                </div>
            </div>
            <div class="col-md-4 chrt_grid" style="visibility: visible; -webkit-animation-delay: 0.4s;">
