                    <h3>Emergency Child Protection & Rehabilitation Centers </h3>
                    <p>South Sudan’s ongoing conflict and near economic collapse have forced vulnerable children out of homes and become street beggars. If not taken care of, these deserving children will continue to grow in numbers and post threat to security and stability in the country.

                        So, building upon our existing child protection program, we plan on expanding efforts in this area by developing emergency homes for abandoned and orphaned children/youth and providing them access to counseling, educational, and other rehabilitation services.   </p>
                </div>
            </div>
