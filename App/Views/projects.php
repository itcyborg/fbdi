                    <h3>Livelihood Training and Access to Microfinance </h3>
                    <p>According to a study conducted by Mercy Corps (2014), most of South Sudanese youth are illequipped and lack basic skills they need to succeed in today’s competitive global market. To help reverse this trend, we plan on providing our vulnerable youth with entrepreneurial and livelihood skills training and access to microfinance and other important financial services. We plan to partner with other civil society organizations as well as South Sudan Microfinance and the Cooperative Bank of South Sudan to socially and economically empower these youth.</p>
